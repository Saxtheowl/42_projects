#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*a;
	t_stack	*b;

	if (argc < 2)
		return (0);
	a = parse_args(argc, argv);
	b = NULL;
	if (!a)
		error_exit();
	sort_stack(&a, &b);
	free_stack(&a);
	free_stack(&b);
	return (0);
}
